    private void saveFile(){
        JFileChooser file = new JFileChooser();
        file.showSaveDialog(null);
        AssignmentConformationUI alert = new AssignmentConformationUI(file.getSelectedFile().toString());
        alert.setVisible(true);
    }
    
    
    private JPanel getPanel(String panelName){
        if(panelName.equalsIgnoreCase("courseBar")){
            return courseBar;
        } 
        else if(panelName.equalsIgnoreCase("feesBar")){
            return submission;
        } else if(panelName.equalsIgnoreCase("assignmentBar")){
            return assignmentBar;
        } 
        
        return new JPanel();
    }
    
    private void setActiveBar(String pre, String next){
        activeSideBar=next;
        resetColor(getPanel(pre));
        setColor(getPanel(next));
    }
    
    private void setColor(javax.swing.JPanel givenPanel){
        givenPanel.setBackground(new Color(69,90,125));  
    }
    
    private void resetColor(javax.swing.JPanel givenPanel){
        givenPanel.setBackground(new Color(69,87,113));  
    }
    
    
